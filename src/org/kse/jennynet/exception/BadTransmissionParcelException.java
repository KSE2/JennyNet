package org.kse.jennynet.exception;

public class BadTransmissionParcelException extends JennyNetException {

   public BadTransmissionParcelException() {
   }

   public BadTransmissionParcelException(String message, Throwable cause) {
      super(message, cause);
   }

   public BadTransmissionParcelException(String message) {
      super(message);
   }

   public BadTransmissionParcelException(Throwable cause) {
      super(cause);
   }

}
