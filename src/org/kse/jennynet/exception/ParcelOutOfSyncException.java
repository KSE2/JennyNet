package org.kse.jennynet.exception;

public class ParcelOutOfSyncException extends JennyNetException {

   public ParcelOutOfSyncException() {
   }

   public ParcelOutOfSyncException(String message, Throwable cause) {
      super(message, cause);
   }

   public ParcelOutOfSyncException(String message) {
      super(message);
   }

   public ParcelOutOfSyncException(Throwable cause) {
      super(cause);
   }

}
